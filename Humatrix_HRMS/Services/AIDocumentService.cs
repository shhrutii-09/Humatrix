    private string GenerateFallbackContent(string prompt)
    {
        return @"<h2>Document</h2><p>Document content will be generated.</p>";
    }

    private string ExtractContentFromResponse(string response)
    {
        // Clean up AI response - remove any markdown code blocks
        var cleaned = response.Replace("```html", "").Replace("```", "").Trim();
        return cleaned;
    }

    private List<AIDocumentSuggestion> ParseSuggestionsFromResponse(string response)
    {
        try
        {
            var suggestions = JsonSerializer.Deserialize<List<AIDocumentSuggestion>>(response);
            return suggestions ?? new List<AIDocumentSuggestion>();
        }
        catch
        {
            return new List<AIDocumentSuggestion>();
        }
    }

    private void AddRuleBasedSuggestions(Employee employee, List<AIDocumentSuggestion> suggestions)
    {
        var tenureDays = (DateTime.UtcNow - employee.JoiningDate).Days;

        // Rule: If employee joined within last 7 days, suggest Offer Letter
        if (tenureDays <= 7 && !suggestions.Any(s => s.DocumentType == "Offer Letter"))
        {
            suggestions.Add(new AIDocumentSuggestion
            {
                DocumentType = "Offer Letter",
                Reason = "Employee joined recently - Offer Letter pending",
                Priority = 10,
                SuggestedData = new Dictionary<string, string>
                {
                    { "JoiningDate", employee.JoiningDate.ToString("dd MMM yyyy") },
                    { "Designation", employee.Designation?.Name ?? "" },
                    { "Department", employee.Department?.Name ?? "" }
                }
            });
        }

        // Rule: After 6 months, suggest Confirmation Letter
        if (tenureDays >= 180 && tenureDays <= 190)
        {
            suggestions.Add(new AIDocumentSuggestion
            {
                DocumentType = "Confirmation Letter",
                Reason = "Probation period completed - Confirmation Letter needed",
                Priority = 8,
                SuggestedData = new Dictionary<string, string>
                {
                    { "ConfirmationDate", DateTime.UtcNow.AddDays(7).ToString("dd MMM yyyy") }
                }
            });
        }

        // Rule: If employee's last working day is set, suggest Experience Letter
        if (employee.LastWorkingDay.HasValue && employee.LastWorkingDay.Value <= DateTime.UtcNow.AddDays(15))
        {
            suggestions.Add(new AIDocumentSuggestion
            {
                DocumentType = "Experience Letter",
                Reason = "Employee is exiting - Experience Letter needed",
                Priority = 9,
                SuggestedData = new Dictionary<string, string>
                {
                    { "LastWorkingDay", employee.LastWorkingDay.Value.ToString("dd MMM yyyy") }
                }
            });
        }
    }

    // OpenAI Response Models
    private class OpenAIResponse
    {
        public List<Choice> choices { get; set; } = new();
    }

    private class Choice
    {
        public Message message { get; set; } = new();
    }

    private class Message
    {
        public string content { get; set; } = string.Empty;
    }
}