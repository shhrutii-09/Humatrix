    public async Task<string> GenerateDocumentContentAsync(
        string templateName,
        string category,
        Employee employee,
        Dictionary<string, string>? customData,
        string generatedBy)
    {
        // Build the prompt for AI
        var prompt = BuildPrompt(templateName, category, employee, customData, generatedBy);

        // Call AI API (OpenAI/Azure OpenAI)
        var aiResponse = await CallAIApiAsync(prompt);

        // Extract and return the generated content
        return ExtractContentFromResponse(aiResponse);
    }

    public async Task<List<AIDocumentSuggestion>> SuggestDocumentsAsync(Employee employee)
    {
        var suggestions = new List<AIDocumentSuggestion>();

        // Calculate tenure in days
        var tenureDays = (DateTime.UtcNow - employee.JoiningDate).Days;

        // AI-powered suggestions based on employee data
        var prompt = $@"
        Analyze this employee and suggest document types that should be generated:
        - Name: {employee.FirstName} {employee.LastName}
        - Designation: {employee.Designation?.Name ?? "N/A"}
        - Department: {employee.Department?.Name ?? "N/A"}
        - Joining Date: {employee.JoiningDate:dd MMM yyyy}
        - Tenure: {tenureDays} days
        - Status: {employee.Status}

        Return JSON array of suggestions with: documentType, reason, priority (1-10)
        ";

        var aiResponse = await CallAIApiAsync(prompt);
        suggestions = ParseSuggestionsFromResponse(aiResponse);

        // Add rule-based suggestions as fallback
        AddRuleBasedSuggestions(employee, suggestions);

        return suggestions;
    }

    public async Task<string> GenerateNotificationMessageAsync(
     string documentName,
     Employee employee,
     string documentNumber)
    {
        var prompt = $@"
Generate a VERY SHORT (max 50 words), professional notification message for an employee.

Document: {documentName}
Employee: {employee.FirstName}
Document Number: {documentNumber}

Requirements:
- Plain text ONLY (NO HTML, NO markdown, NO special characters)
- 1-2 sentences maximum
- Warm but professional tone
- Include call to action to view document

Example: Dear {employee.FirstName}, your {documentName} is ready. Please review it in the Documents section.
";

        var response = await CallAIApiAsync(prompt);

        // Strip all HTML and special characters
        var cleanResponse = System.Text.RegularExpressions.Regex.Replace(response, @"<[^>]*>", "");
        cleanResponse = cleanResponse.Replace("```", "").Replace("\n", " ").Trim();

        // Limit to 200 characters
        if (cleanResponse.Length > 200)
            cleanResponse = cleanResponse.Substring(0, 197) + "...";

        return string.IsNullOrEmpty(cleanResponse) ? $"Your {documentName} is ready. View in Documents." : cleanResponse;
    }

    // ==================== PRIVATE METHODS ====================

    private string BuildPrompt(
        string templateName,
        string category,
        Employee employee,
        Dictionary<string, string>? customData,
        string generatedBy)
    {
        var employeeInfo = $@"
        EMPLOYEE INFORMATION:
        - Full Name: {employee.FirstName} {employee.LastName}
        - Designation: {employee.Designation?.Name ?? "N/A"}
        - Department: {employee.Department?.Name ?? "N/A"}
        - Employee Code: {employee.EmployeeCode ?? "N/A"}
        - Joining Date: {employee.JoiningDate:dd MMM yyyy}
        - Gender: {employee.Gender ?? "N/A"}
        ";

        var customDataInfo = customData != null && customData.Any()
            ? $"ADDITIONAL DATA: {JsonSerializer.Serialize(customData)}"
            : "";

        return $@"
        You are an HR document generator. Create a professional {templateName} ({category}) for the following employee.

        {employeeInfo}
        {customDataInfo}

        Generated by: {generatedBy}
        Current Date: {DateTime.UtcNow:dd MMM yyyy}

        RULES:
        1. Use professional, warm, and respectful tone
        2. Include all relevant employee details naturally in the content
        3. For Offer Letter: Include joining date, designation, department, probation period
        4. For Warning Letter: Be firm but fair, include issue details and expected actions
        5. For Appreciation Letter: Be encouraging and specific about achievements
        6. For Experience Letter: Highlight tenure, role, and positive attributes
        7. Format as HTML with proper headings and paragraphs
        8. DO NOT include any explanations, just return the HTML content

        Generate the complete document content:
        ";
    }

    private async Task<string> CallAIApiAsync(string prompt)
    {
        var apiKey = _configuration["OpenAI:ApiKey"];
        var baseUrl = _configuration["OpenAI:BaseUrl"] ?? "https://api.groq.com/openai/v1";
        var model = _configuration["OpenAI:Model"] ?? "llama-3.3-70b-versatile";

        if (string.IsNullOrEmpty(apiKey))
        {
            return GenerateFallbackContent(prompt);
        }

        // Groq uses OpenAI-compatible format
        using var client = _httpClientFactory.CreateClient();
        client.DefaultRequestHeaders.Add("Authorization", $"Bearer {apiKey}");

        var requestBody = new
        {
            model = model,
            messages = new[]
            {
            new { role = "system", content = "You are an HR document generation assistant. Return ONLY HTML content, no explanations." },
            new { role = "user", content = prompt }
        },
            temperature = 0.7,
            max_tokens = 2000
        };

        var response = await client.PostAsync(
            $"{baseUrl}/chat/completions",
            new StringContent(JsonSerializer.Serialize(requestBody), Encoding.UTF8, "application/json"));

        var jsonResponse = await response.Content.ReadAsStringAsync();

        if (!response.IsSuccessStatusCode)
        {
            _logger.LogWarning($"Groq API error: {jsonResponse}");
            return GenerateFallbackContent(prompt);
        }

        using var doc = JsonDocument.Parse(jsonResponse);
        var content = doc.RootElement
            .GetProperty("choices")[0]
            .GetProperty("message")
            .GetProperty("content")
            .GetString();

        return content ?? GenerateFallbackContent(prompt);
    }

